package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		// Primitive data types are int,double,char, boolean
		// 1. int 
		int i=10;
		i=20;
		int j=30;
		int k=-50;
		//2. double
		double d=12.33;
		double d1=24.33;
		double d2=100;
		//3. char
		char c1='S';
		char c2=83; //prints D
		char c3='1';
		//4. boolean
		boolean b1=true;
		boolean b2=false;
		//5. String is a class not a datatype and can be used as a data type
		String s="Sandhya";
		String s1="This is my java code";
		String s3="1000"; // 1000 is a string
		String s4="26.03";
		System.out.println("i+j="+ (i+j));
		System.out.println("ascii of 83 is "+c2);
		System.out.println(c1);
		System.out.println(s1);
		System.out.println(s);
	}

}
